package org.wso2.transport.http.netty.exception;

/**
 * This class represents Unknown WebSocket frame exception.
 */
public class UnknownWebSocketFrameTypeException extends Exception {

    /**
     * Constructs a new exception with the specified detail message.  The
     * cause is not initialized, and may subsequently be initialized by
     * a call to {@link #initCause}.
     *
     * @param message the detail message. The detail message is saved for later retrieval by the {@link #getMessage()}
     *                method.
     */
    public UnknownWebSocketFrameTypeException(String message) {
        super(message);
    }
}
